//用于存储登录信息 创建user类
public class userInformation {
    String hostName;
    String password;
    String name;
    String sex;

    void setHostName(String setHostName){
        this.hostName = setHostName;
    }
    void setPassword(String password){
        this.password = password;
    }
    void setName(String name){
        this.name = name;
    }
    void setSex(String sex){
        this.sex = sex;
    }
}
